int	ft_isalnum(int c)
{
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z') || (c >= 'a'
			&& c <= 'z'))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}

#include <stdio.h>

int	main(void)
{
	printf("%d\n", ft_isalnum('1'));
}